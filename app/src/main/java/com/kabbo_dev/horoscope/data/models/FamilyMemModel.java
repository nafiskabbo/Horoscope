package com.kabbo_dev.horoscope.data.models;

public class FamilyMemModel {
    String name, status;

    public FamilyMemModel(String name, String status) {
        this.name = name;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
